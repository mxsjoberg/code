10 + 20                     // 30
20 - 10                     // 10
10 * 20                     // 200
20 / 10                     // 2

// floating point conversion
10.0 + (10 + 20)            // 40.0
20.0 - (10 + 10)            // 0.0
10.0 * (10 * 2)             // 200.0

// common division error
30 / 20                     // 1
30.0 / 20.0                 // 1.5

// modulo, remainder
12.5 % 10.0                 // 2.5
10.0 % 20.0                 // 10.0